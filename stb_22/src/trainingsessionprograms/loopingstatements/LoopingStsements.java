package loopingstatements;

public class LoopingStsements {

	public static void main(String[] args) {
		int count =1;
		while(count<10)
		{
			System.out.println("Hello Java");
			count++;
		}
		
		int i=1;
		while(i<=5)
		{
			System.out.println("i:"+i);
			i++;
		}
		System.out.println("Out of loop at i="+i);
	}

}
